<p  style="padding-top:5%;">&#8211; Bò kho có thể ăn với bánh  mì, hủ tiếu (hoặc cơm hay mì tùy thích). Nếu để ăn với hủ tiếu, các bạn có thể thêm nước. Còn nếu muốn dùng với bánh mì thì làm cho nước thịt sánh hơn bằng cách sau: hòa tan khoảng 2 thìa cafe bột mì hay bột năng trong 20 &#8211; 30 ml nước. Khi thịt bò đã chín mềm thì để lửa nhỏ, từ từ đổ hỗn hợp bột hòa tan trong nước vào nồi thịt, vừa đổ vừa quấy đều. Khi thấy nước trong nồi đủ sệt như ý muốn thì dừng lại (không nhất thiết phải dùng hết chỗ nước bột).</p>
<p>* Yêu cầu thành phẩm: Thịt bò chín mềm nhưng không bị bã, ngấm gia vị mặn ngọt đậm đà, nước dùng có màu đỏ sánh đẹp, dậy mùi thịt bò quyện với cà chua và ngũ vị hương.</p>
<p>Chúc cả nhà cuối tuần thật vui và thoải mái! ^.^</p>
